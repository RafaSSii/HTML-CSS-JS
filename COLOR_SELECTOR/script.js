function color(valor){
var color_show = document.getElementById("color-show");


if(valor == "red"){
    color_show.style.backgroundColor = 'red';
    color_show.innerText = 'RED'
}
if(valor == "yellow"){
    color_show.style.backgroundColor = 'yellow';
    color_show.innerText = 'YELLOW'
    
}
 if(valor == "blue"){
    color_show.style.backgroundColor = 'blue';
    color_show.innerText = 'BLUE'
}
 if(valor == "green"){
    color_show.style.backgroundColor = 'green';
    color_show.innerText = 'GREEN'
}



}

    
    



